    private void jbsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbsimpanActionPerformed
        String kode, judul, pengarang, penerbit, isbn, jenis, tahunterbit, kategori, hargabeli, stock;
        kode = txtkode.getText();
        judul = txtjudul.getText();
        pengarang = txtpengarang.getText();
        penerbit = txtpenerbit.getText();
        isbn = txtisbn.getText();
        tahunterbit = txttahunterbit.getText();
        hargabeli = txthargabeli.getText();
        stock = txtstock.getText();
        //combo box
        kategori = String.valueOf(cbkategori.getSelectedItem());
        jenis = String.valueOf(cbjenis.getSelectedItem());
        /*if(("".equals(kode)) && ("".equals(judul))){
            JOptionPane.showMessageDialog(rootPane, "Judul dan Pengarang ksoong");
        }*/
        if(("".equals(kode))){
        //cek masih kosong maka keluarkan pesan error, kosongkan field
            JOptionPane.showMessageDialog(rootPane, "Anda belum menginputkan kode buku", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
        else if("".equals(judul)){
            JOptionPane.showMessageDialog(rootPane, "Anda belum menginputkan Judul Buku", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
        else if("".equals(pengarang)){
            JOptionPane.showMessageDialog(rootPane, "Anda belum menginputkan Pengarang Buku ","Peringatan", JOptionPane.WARNING_MESSAGE);
        }
        else if("".equals(penerbit)){
            JOptionPane.showMessageDialog(rootPane, "Anda belum menginputkan Penerbit Buku", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
        else if("".equals(isbn)){
            JOptionPane.showMessageDialog(rootPane, "Anda belum menginputkan ISBN Buku", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
        else if("".equals(tahunterbit)){
            JOptionPane.showMessageDialog(rootPane, "Anda belum menginputkan Tahun terbit Buku Kosong", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
        else if("".equals(hargabeli)){
            JOptionPane.showMessageDialog(rootPane, "Anda belum menginputkan harga beli Buku", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
        else if("".equals(stock)){
            JOptionPane.showMessageDialog(rootPane, " Stock Kosong", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
        else{
            //
            openDB();
            try {
                stm = con.createStatement();
                rs = stm.executeQuery("select * from buku where kode='"+kode+"'");
                if(rs.next()){
                    JOptionPane.showMessageDialog(rootPane, "Data Buku Sudah Ada!");
                }else{
                    stm = con.createStatement();
                    stm.executeUpdate("insert into buku set kode='"+kode+"', judul='"+judul+"', pengarang='"+pengarang+"', penerbit='"+penerbit+"', isbn='"+isbn+"', jenis='"+jenis+"', tahunterbit='"+tahunterbit+"', kategori='"+kategori+"', hargabeli='"+hargabeli+"', stock='"+stock+"'");
                    JOptionPane.showMessageDialog(rootPane, "Data berhasil tersimpan!"); //simpan ke DB
                    bacaData();
                }
            } catch (SQLException ex) {
                Logger.getLogger(PendataanBuku.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_jbsimpanActionPerformed

