    private void jbhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbhapusActionPerformed
        //get kode dari txtfieldkode
        //hapus dr database where kode =                 
        int konfirmasiHapus = JOptionPane.showConfirmDialog(rootPane, "Data akan dihapus?", "Peringatan!", JOptionPane.YES_NO_OPTION);
        
        if(konfirmasiHapus == JOptionPane.YES_OPTION){
            String kode = txtkode.getText();
            openDB();
            try {
                stm = con.createStatement();
                stm.executeUpdate("delete from buku where kode = '"+kode+"'");
                JOptionPane.showMessageDialog(rootPane, "Buku dengan kode : "+kode+" Telah dihapus!", "Infromation", JOptionPane.INFORMATION_MESSAGE);
                bacaData();
                resetField();
            } catch (SQLException ex) {
                Logger.getLogger(PendataanBuku.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
        
        }
    }//GEN-LAST:event_jbhapusActionPerformed

