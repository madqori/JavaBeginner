    private void bacaData() {
        openDB();
        try {
            stm = con.createStatement();
            rs=stm.executeQuery("Select * FROM buku");
            java.sql.ResultSetMetaData meta=rs.getMetaData();
            int col=meta.getColumnCount();
            int baris=0;
            String jbt;
            while(rs.next()){
                baris=rs.getRow();
            }
            
            tabelData=new Object[baris][col];
            int x=0;
            String kb=null;
            rs.beforeFirst();
            while(rs.next()){
                tabelData[x][0]=rs.getString("kode"); 
                tabelData[x][1]=rs.getString("judul");
                tabelData[x][2]=rs.getString("pengarang");
                tabelData[x][3]=rs.getString("penerbit");
                tabelData[x][4]=rs.getString("isbn");
                tabelData[x][5]=rs.getString("jenis");
                tabelData[x][6]=rs.getString("tahunterbit");
                tabelData[x][7]=rs.getString("kategori");
                tabelData[x][8]=rs.getString("hargabeli");
                tabelData[x][9]=rs.getString("stock");
                x++;
            }
            
            tblHasil.setModel(new DefaultTableModel(tabelData,header));
            
        } catch (SQLException ex) {
            Logger.getLogger(PendataanBuku.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

